uint8_t * hex2str(unsigned char *inchar, unsigned int len)
{
    unsigned char hbit,lbit;
    unsigned int i;
	  for(i=0;i<len;i++)
		{
	  	hbit = (*(inchar+i)&0xf0)>>4;
	    lbit = *(inchar+i)&0x0f;
	    if (hbit>9) printBuf[2*i]='A'+hbit-10;
	  	else printBuf[2*i]='0'+hbit;
	    if (lbit>9) printBuf[2*i+1]='A'+lbit-10;
	  	else    printBuf[2*i+1]='0'+lbit;
	  }
		printBuf[2*i] = 0;
		return printBuf;
}
void PrintHex(unsigned char *inchar, unsigned int len)
{
	PrintChar(hex2str(inchar, len));
}

